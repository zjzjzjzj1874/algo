func random() {
	sc := bufio.NewScanner(os.Stdin)
	sc.Scan()
	con := sc.Text()
	num, _ := strconv.Atoi(con)
	nums := make([]int, 500)
	for num > 0 && sc.Scan() {
		con := sc.Text()
		n, _ := strconv.Atoi(con)
		nums[n-1]++
		num--
	}

	for i, num := range nums {
		if num > 0 {
			fmt.Println(i + 1)
		}
	}
}

